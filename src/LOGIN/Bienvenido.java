private class hilo extends Thread {

        @Override
        public void run() {
            try {
                while (true) {
                    auxiliar++;
                    barra.setValue(auxiliar);
                    repaint();
                    switch (auxiliar) {
                        case 0:
                            lbl_texto.setText("Cargando programa....");
                            break;
                        case 10:
                            lbl_texto.setText("Leyendo preferencias....");
                            break;
                        case 20:
                            lbl_texto.setText("Cargando m√≥dulos....");
                            break;
                        case 30:
                            lbl_texto.setText("Verificando sesiones....");
                            break;
                        case 40:
                            lbl_texto.setText("Carga finalizada....");
                            break;
                        case 50:
                            Login p = new Login() ;
                            p.setVisible(true);
                            p.setLocationRelativeTo(null);
                            Bienvenido.this.dispose();
                            break;
                    }
                    Thread.sleep(15);
                }

            } catch (Exception e) {
            }

        }
    }


