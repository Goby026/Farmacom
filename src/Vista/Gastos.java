    public void loadGastos(){
        String sql = "SELECT `id_gastos`, `descripcion_gasto`, `monto`, `fecha_gasto` FROM `tgastos` WHERE 1";
        try {
            String datos[] = new String[4];
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {                
                datos[0] = rs.getString("id_gastos");
                datos[1] = rs.getString("descripcion_gasto");
                datos[2] = rs.getString("monto");
                datos[3] = rs.getString("fecha_gasto");
                model_gastos.addRow(datos);
            }
            tbl_gastos.setModel(model_gastos);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(getRootPane(), e.getMessage());
        }
    }
    
    public void saveGastos(){
        String concepto = txt_concepto.getText();
        String fecha = new ManejadorFechas().getFechaActualMySQL();
        double monto = Double.parseDouble(txt_monto.getText());
        
        String sql = "INSERT INTO tgastos(`descripcion_gasto`, `monto`, `fecha_gasto`) VALUES ( '"+concepto+"', "+monto+", '"+fecha+"' )";
        try {
            Statement st = con.createStatement();
            int res = st.executeUpdate(sql);
            
            if (res>0) {
                JOptionPane.showMessageDialog(getRootPane(), "Gasto registrado");
            } else {
            }
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(getRootPane(), e.getMessage());
        }
        
    }
    
    private void filaSeleccionada(){
        cmb_modificar.setEnabled(true);
        int fila = tbl_gastos.getSelectedRow();
        lbl_id.setText(tbl_gastos.getValueAt(fila, 0).toString());
        String concepto = tbl_gastos.getValueAt(fila, 1).toString();
        double monto = Double.parseDouble(tbl_gastos.getValueAt(fila, 2).toString());
        String fecha = tbl_gastos.getValueAt(fila, 3).toString();
        txt_modConcepto.setText(concepto);
        txt_modMonto.setText(""+monto);
    }
    
    public void bloquear(){
        cmb_modificar.setEnabled(false);
        txt_modConcepto.setEnabled(false);
        txt_modMonto.setEnabled(false);
        btn_save.setEnabled(false);
        jdc_fecha.setEnabled(false);
    }
    
    public void desbloquear(){
        cmb_modificar.setEnabled(true);
        txt_modConcepto.setEnabled(true);
        txt_modMonto.setEnabled(true);
        btn_save.setEnabled(true);
        jdc_fecha.setEnabled(true);
        txt_modConcepto.requestFocus();
    }
    
    private void limpiarCampos() {
        txt_concepto.setText("");
        txt_monto.setText("");
        txt_modConcepto.setText("");
        txt_modMonto.setText("");
        lbl_id.setText("...");
        txt_concepto.requestFocus();
    }
    
    public void limpiarTabla() {
        for (int i = 0; i < tbl_gastos.getRowCount(); i++) {
            model_gastos.removeRow(i);
            i -= 1;
        }
    }

