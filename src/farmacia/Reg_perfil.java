    private void btn_guardar_perfilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardar_perfilActionPerformed
        String p = txt_perfil.getText();
        
        String sql = "INSERT INTO tperfil( descrip_perfil, priv_perfil) VALUES ('"+p+"','"+privi+"')";
        try {
            Statement st = con.createStatement();
            int res = st.executeUpdate(sql);
            if (res > 0 ) {
                JOptionPane.showMessageDialog(null, "PERFIL REGISTRADO");
                
            } else {
            }
            
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }//GEN-LAST:event_btn_guardar_perfilActionPerformed

