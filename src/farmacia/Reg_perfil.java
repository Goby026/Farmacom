    private void chx_totalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chx_totalActionPerformed
        if (chx_total.isSelected()) {
            chx_total.setForeground(Color.green);
            chx_limitado.setForeground(Color.RED);
            chx_limitado.setEnabled(false);
            privi = "TOTAL";
            
        } else {
            chx_total.setForeground(Color.black);
            chx_limitado.setForeground(Color.black);
            chx_limitado.setEnabled(true);
        }
    }//GEN-LAST:event_chx_totalActionPerformed

